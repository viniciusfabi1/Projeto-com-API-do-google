                    <h2>Tráfico de Drogas</h2>
                    <p>De acordo com o artigo 33 da Lei de Drogas, pratica o crime de tráfico de drogas quem importa, exporta, remete, prepara, produz, fabrica, adquire, vende, expõe à venda, oferece, tem em depósito, transporta, traz consigo, guarda, prescreve, ministra, entrega a consumo ou fornece drogas,
					Além do tráfico de drogas, há outros tipos de tráficos ilícitos, podendo ser eles, armas, influências, animais, pessoas, orgãos, entre outros.</p>
					<h2>Furto</h2>
					<p>O crime de furto está previsto no artigo 155 do Código Penal Brasileiro, Furto é o ato de retirar algo que pertence por direito a outra pessoa, contra a vontade desta, mas sem o uso de violência contra a vítima. O furto é normalmente praticado às escondidas, para que o ladrão não seja notado.
						O Código Penal ainda tipifica como furto qualificado o crime de furto que envolva, destruição ou rompimento de obstáculo à subtração da coisa, abuso de confiança, ou mediante fraude, escalada ou destreza,
						emprego de chave falsa, mediante concurso de duas ou mais pessoas.
					</p>
					<h2>Assalto / Roubo</h2>
					<p>O crime de roubo está fundamentado no artigo 157 do Código Penal Brasileiro. Roubo é o crime que consiste na apropriação indevida de um bem móvel alheio, mediante uso de violência ou ameaça grave da vítima por parte do criminoso.
					</p>
					<h2>Assalto a Mão Armada</h2>
					<p>Já sabemos o que é Assalto, o Assalto a mão armada é bem intuitivo como o próprio nome mesmo já diz, o Assalto a mão armada é quando uma pessao exerce uma força para se enponderar de algo que não é de seu pertence usando qualquer tipo de arma 
						para obter sucesso na ação, A pena para este crime pode variar entre 4 (quatro) a 10 (dez) anos de reclusão, mais o pagamento de multa. A penalização pode ser agravada de 1/3 (um terço) até a metade se o roubo for feito mediante:
						uso de arma, participação de duas ou mais pessoas na execução do crime, se a vítima estiver em serviço de transporte de valores e o criminoso tiver o conhecimento disso, subtração de veículo automotor que venha a ser transportado para outro estado ou país, 
						manter a vítima como refém, restringindo a sua liberdade.
					</p>
					<h2>Roubo de Estabelecimento</h2>
					<p>Constítui em um roubo de Estabelecimento, quando uma pessoa entra em um local de vigência pública ou privada, com intuito de roubar qualquer tipo de pertence que não seja de sua posse.</p>
